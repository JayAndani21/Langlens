  Widget _buildSocialIcon(String assetPath) {
    return InkWell(
      onTap: () {},
      child: CircleAvatar(
        radius: 18,
        backgroundColor: const Color.fromARGB(255, 2, 0, 0),
        child:
            Image.asset(assetPath, width: 20, height: 20, color: Colors.white),
      ),
    );
  }
}
